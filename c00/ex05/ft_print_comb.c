#include <unistd.h>

void	print_nbr(int n[3])
{
	int	i;

	i = -1;
	while (++i < 3)
		write(1, &(char){n[i] + 48}, 1);
	if (n[0] != 7)
		write(1, ", ", 2);
}

void	ft_print_comb(void)
{
	int	n[3];

	n[0] = -1;
	n[1] = 1;
	while (++n[0] <= 7)
	{
		n[1] = n[0] + 1;
		while (n[1] <= 8)
		{
			n[2] = n[1] + 1;
			while (n[2] <= 9)
			{
				print_nbr(n);
				n[2]++;
			}
			n[1]++;
		}
	}
}
